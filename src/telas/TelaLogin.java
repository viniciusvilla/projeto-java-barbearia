    public void logar(){
        //Resgatando login e senha digitados pelo usuario e executando na query SQL
        String sql = "select * from tb_barbeiros where login = ? and senha = ?";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtLogin.getText());
            String senha = new String(txtSenha.getPassword());
            
            pst.setString(2, senha);
            
            rs = pst.executeQuery();
            
            //Verificando se o Login e senha existem no BD
            if(rs.next() == true){
                TelaPrincipal principal = new TelaPrincipal();
                
                String perfil = rs.getString(7);
                
                //Tratamento de nivel de acesso Administrador
                if(perfil.equals("admin")){
                    principal.mBarbeiro.setEnabled(true);
                }
                
                String id_barbeiro = rs.getString(1);
                
                String usu = rs.getString(2);
                System.out.println(usu);
                
                principal.lblPerfil.setText(perfil);
                principal.lblUsu.setText(usu);
                principal.lblId_b.setText(id_barbeiro);
                
                principal.setVisible(true);
                this.dispose();
                conexao.close();
                
            } else{
                JOptionPane.showMessageDialog(null, "[ERRO] Login ou Senha inv√°lidos.");
            }
           
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    

